package com.netcracker.ca.model;

public class BugReport {

	private String title;
	
	private String body;
	
	public BugReport() {}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}
	
}
